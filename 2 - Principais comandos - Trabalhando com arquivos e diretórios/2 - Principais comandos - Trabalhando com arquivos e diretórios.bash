Diretórios
    / -> Diretório raiz a exemplo do C: no windows
    bin/sbin -> Programas e comandos que execumtamos no dia dia
    etc -> Arquivos de configuração
    lib -> Bibliotecas do sistema
    root -> Diretório do usuário root
    opt -> Softwares de terceiros exemplo: Google Chrome
    proc -> Arquivos temporarios
    mnt -> Monta drives cdroom, hd externo
    tmp -> Arquivos temporários
    ust -> Instalações do sistema (lock, logs)
    srv -> Serviços
    home -> Um diretório para cado usuário do sistema



